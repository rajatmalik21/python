str='Hello World'

print(str)           #will print the entire string
print(str[0])        #will print the first character of a string
print(str[2:5])      #will print the 3rd to 5th character of a string
print(str[2:])       #will print string from 3rd character onwards
print(str * 2)       #will print string twice
print(str + "TEST")  #will concatenate strings and print it
